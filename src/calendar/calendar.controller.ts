import {
  Controller,
  Get,
  Post,
  Body,
  Param,
  Delete,
  Patch,
  ParseIntPipe,
} from '@nestjs/common';
import { CalendarEventService } from './calendar.service';
import { CreateCalendarEventDto } from './dto/create-calendar.dto';
import { UpdateCalendarEventDto } from './dto/update-calendar.dto';

@Controller('calendar-events')
export class CalendarEventController {
  constructor(private readonly service: CalendarEventService) {}

  @Post()
  create(@Body() dto: CreateCalendarEventDto) {
    return this.service.create(dto);
  }

  @Get()
  findAll() {
    return this.service.findAll();
  }

  @Get(':id')
  findOne(@Param('id', ParseIntPipe) id: number) {
    return this.service.findOne(id);
  }

  @Patch(':id')
  update(
    @Param('id', ParseIntPipe) id: number,
    @Body() dto: UpdateCalendarEventDto,
  ) {
    return this.service.update(id, dto);
  }

  @Delete(':id')
  remove(@Param('id', ParseIntPipe) id: number) {
    return this.service.remove(id);
  }
}